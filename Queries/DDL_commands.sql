create table Customer 
(
	customer_name varchar(50),
    customer_id int not NULL auto_increment,
    phone_number varchar(50) not NULL,
    PRIMARY KEY (customer_id)
);

create table Customer_Location 
(
	customer_id int not NULL,
    flat_no int not NULL,
    street varchar(50),
    city varchar(50),
    state varchar(50),
    PRIMARY KEY(customer_id, flat_no),
    Constraint fk_1 FOREIGN KEY(customer_id) REFERENCES Customer(customer_id)
    On Update Cascade
    On Delete Cascade
);

create table Seller 
(
	seller_name varchar(50),
    seller_id int not NULL auto_increment,
    phone_number varchar(50) not NULL,
    PRIMARY KEY (seller_id)
);

create table Seller_Location 
(
	seller_id int not NULL,
    factory_no int,
    street varchar(50),
    city varchar(50),
    state varchar(50),
    PRIMARY KEY(seller_id, factory_no),
    Constraint fk_2 FOREIGN KEY(seller_id) REFERENCES Seller(seller_id)
    On Update Cascade
    On Delete Cascade
);

create table Items 
(
	item_name varchar(50) NOT NULL,
    item_id int not NULL auto_increment,
    category varchar(20),
    PRIMARY KEY (item_id)
);

create table Seller_Inventory 
(
	item_name varchar(50) not NULL,
    item_id int not NULL,
	seller_id int not NULL,
    price int not NULL,
    quantity int not NULL,
    PRIMARY KEY(seller_id, item_id),
    Constraint fk_3 FOREIGN KEY (item_id) REFERENCES Items(item_id)
    On Update Cascade
    On Delete Cascade,
    Constraint fk_4 FOREIGN KEY (seller_id) REFERENCES Seller(seller_id)
    On Update Cascade
    On Delete Cascade
);

create table Employee
(
	employee_name varchar(50) not NULL,
    employee_id int not NULL auto_increment,
    item_id int not NULL,
    PRIMARY KEY(employee_id, item_id),
    Constraint fk_14 FOREIGN KEY (item_id) REFERENCES Items(item_id)
    On Update Cascade
    On Delete Cascade
);

create table not_in_stock 
(
    item_id int not NULL,
    expected_date date,
    PRIMARY KEY (item_id),
    Constraint fk_5 FOREIGN KEY (item_id) REFERENCES Items(item_id) 
    On Update Cascade
    On Delete Cascade
);

create table InStock 
(
	item_id int not NULL,
    quantity int not NULL,
    PRIMARY KEY (item_id),
	Constraint fk_6 FOREIGN KEY (item_id) REFERENCES Items(item_id)
    On Update Cascade
    On Delete Cascade
);

create table Payment 
(
	customer_id int not NULL,
    payment_mode varchar(30) not NULL,
    PRIMARY KEY(customer_id, payment_mode),
    Constraint fk_7 FOREIGN KEY(customer_id) REFERENCES Customer(customer_id)
    On Update Cascade
    On Delete Cascade
);

create table Bill 
(
	bill_id int not NULL auto_increment,
    customer_id int not NULL,
    seller_id int not NULL,
    item_id int not NULL,
    price int not NULL,
    quantity int not NULL,
    PRIMARY KEY(bill_id),
    Constraint fk_8 FOREIGN KEY (seller_id) REFERENCES Seller_Inventory(seller_id),
    Constraint fk_9 FOREIGN KEY (customer_id) REFERENCES Payment(customer_id),
    Constraint fk_10 FOREIGN KEY (item_id) REFERENCES InStock(item_id)
    On Update Cascade
    On Delete Cascade
);

create table Deliver
(
	customer_id int not NULL,
	seller_id int not NULL,
    bill_id int not NULL,
    flat_no int,
    factory_no int,
    PRIMARY KEY(bill_id),
    Constraint fk_11 FOREIGN KEY (customer_id, flat_no) References Customer_Location(customer_id, flat_no),
    Constraint fk_12 FOREIGN KEY (seller_id, factory_no) References Seller_Location(seller_id, factory_no),
    Constraint fk_13 FOREIGN KEY(bill_id) REFERENCES Bill(bill_id)
    On Update Cascade
    On Delete Cascade
);

